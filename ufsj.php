      <div id="expand" class="content-text-container padNone content-expand">
        <span class="content-paragraph">
          <br>
          The Federal University of São João del-Rei (UFSJ) originated from the association of the three
          institutions of higher education existing in São João del-Rei in the 1980s: Dom Bosco (School of
          Philosophy, Sciences and Language), the Municipal Foundation (School of Economics, Administration
          and Accounting), and the School of Industrial Engineering.  The Foundation of Higher Education of
          São João del-Rei (FUNREI), was created in December 18, 1986 by Brazil’s former President Jose Sarney.
          Finally, on April 19, 2002, the institution was transformed into a federal university.<br>
          <br>
          Ranked among Brazil’s top Institutions of Higher Education and located at a short distance from the state capital
          city of Belo Horizonte, UFSJ offers a wide selection of over 50 graduate and undergraduate courses in areas such
          as Health Sciences, Arts and Humanities, Music, Social Sciences, Engineering and Technology, and Basic Sciences.
          UFSJ has three campuses in São João del-Rei: Santo Antônio, Dom Bosco, and Tancredo Neves, and a Cultural Center:
          “Solar da Baronesa.” In São João del-Rei, the following undergraduate courses are offered: Business Administration,
          Life Sciences, Accounting, Economics, Physical Education, Industrial Electrical Engineering, Industrial Mechanical
          Engineering, Philosophy, Physics, History, Literature, Mathematics, Education, Psychology, Chemistry, and Music.
          In São João del-Rei, UFSJ offers Graduate Programs such as Chemistry and Physics and a Masters in Literary Theory
          and Cultural Criticism. In 2008, UFSJ started to offer Masters degrees in Education, Psychology, History, and Energy
          Engineering. Since 1997, the Federal University of São João del-Rei has been offering specialization courses in
          several areas, such as Business Administration, Economics and Agricultural Business Management, Mathematics,
          Philosophy, and History.<br>
          <br>
          A fourth campus has been implemented to meet the expansion project of the federal government. It is located in
          Alto Paraopeba in the city of Ouro Branco. It started in 2008 and offers five undergraduate courses in the field
          of Engineering: Civil Engineering, Mechatronics, Telecommunication Engineering, and Chemical Engineering. A fifth
          campus was built in the city of Divinópolis, with four new courses in Health: Medicine, Pharmacy, Nursing, and
          Biochemistry.<br>
          <br>
          With the developments in teaching, research, and extension, UFSJ now has 482 professors and 336 effective technical
          and administrative staff. More teachers and technical-administrative public employees are to work on the new campuses
          of Divinópolis and Alto Paraopeba. The high standard of training of our professional staff and the availability of evening
          courses at UFSJ give this public institution an outstandingly high quality and inclusiveness.<br>
          <br>
          Courses are taught on six different campuses, three of them located in São João del-Rei and the other three in the
          towns of Divinópolis, Ouro Branco, and Sete Lagoas. The University offers a stimulating environment for academic work,
          and at the same time, since it is situated in the historical heart of the state of Minas Gerais, it gives access to a
          range of cultural activities and attractions.
