          abroad programs for undergraduate students since 1987.<br>
          <br>
          The Intensive Portuguese Language program is open to all Portuguese language levels. During this program, students have the
          opportunity to obtain intensive training in Portuguese. Beginning and intermediate students will complete 144 hours of
          Portuguese-language coursework, while advanced students will complete 126 hours of Portuguese-language coursework. Coursework
          is completed in grammar, conversation, written expression and culture. All courses are instructed in Portuguese and focus on
          Portuguese language acquisition. Students are evaluated on their ability to demonstrate level-appropriate command of Portuguese
          grammar and conversation in written assignments and while participating in class presentations and discussions.<br>
          <br>
          During the first 2 weeks of this 7-week program, all levels of Portuguese Language: Oral & Written Comprehension and Expression
          consists of an 18 hour conversation intensive. The remaining 5 weeks will focus on the written and expression component of the course.<br>
          <br>
          All courses are taught at the Universidade do Sul de Santa Catarina by local university professors. ISA students take classes
          alongside other ISA students.<br>
