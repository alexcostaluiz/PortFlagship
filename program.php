      <p class="content-header font20">Background</p>
      <span class="content-paragraph justify-text clearboth">
        The Capstone Year abroad is the PFP's main and most enriching
        study abroad experience. Flagship students completing the Capstone Year spend their first
        6 months taking classes at UFSJ (Universidade Federal de São João del-Rei) and the following
        6 months completing an internship in Brazil specifically tailored to their major and areas
        of interest. Overseas, PFP students can expect fun, challenging classes specifically fitted
        to each student, a rich cultural experience rooted in daily engagement with local students
        and community members, accelerated language study and significant proficiency advancements,
        and organized trips to explore the cultural and natural sites of Brazil.<br>
        <br>
        Once a student has achieved an advanced score on the ACTFL OPI, he or she begins the
        Capstone Year - 6 months at UFSJ in São João del Rei, Minas Gerais, Brazil and 6 months
        at a personalized internship.<br>
        <br>
        It is during the Capstone Year abroad that PFP scholars make the leap from Advanced to
        Superior (i.e. professional working capacity) proficiency in Portuguese and complete a
        final Capstone research project in the target language.<br>
        <br>
        <br>
